import com.karumi.dexter.listener.single.PermissionListener;

import java.io.File;
import java.util.ArrayList;

public class MainActivity extends AppCompatActivity {

    ListView listView;
    String[] items;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        listView = findViewById(R.id.listViewSong);

        checkPermission();
    }

    public void checkPermission() {
        Dexter.withContext(this).withPermission(Manifest.permission.READ_EXTERNAL_STORAGE)
                .withListener(new PermissionListener() {
                    @Override
                    public void onPermissionGranted(PermissionGrantedResponse permissionGrantedResponse) {
                        displaySongs();
                    }

                    @Override
                    public void onPermissionDenied(PermissionDeniedResponse permissionDeniedResponse) {

                    }

                    @Override
                    public void onPermissionRationaleShouldBeShown(PermissionRequest permissionRequest, PermissionToken permissionToken) {
                        permissionToken.continuePermissionRequest();
                    }
                }).check();
    }

    public ArrayList<File> findSong(File datei) {  // Lieder im Smartphone finden
        ArrayList<File> arrayList = new ArrayList<>();

        File[] dateien = datei.listFiles();
        if (dateien != null) {
            for (File singledatei : dateien) {
                if (singledatei.isDirectory() && !singledatei.isHidden()) {
                    arrayList.addAll(findSong(singledatei));
                } else {
                    if (singledatei.getName().endsWith(".mp3") || singledatei.getName().endsWith(".wav") || singledatei.getName().endsWith(".m4a")) {
                        arrayList.add(singledatei);
                    }
                }
            }
        }
        return arrayList;
    }

    void displaySongs() {
        final ArrayList<File> mySongs = findSong(Environment.getExternalStorageDirectory());
        items = new String[mySongs.size()];
        for (int i = 0; i < mySongs.size(); i++) {
            items[i] = mySongs.get(i).getName().toString().replace(".mp3", "")
                    .replace(".wav", "").replace(".m4a", "");

        }

      class customAdapter extends BaseAdapter{

            @Override
            public int getCount() {
                return items.length;
            }

            @Override
            public Object getItem(int position) {
                return null;
            }

            @Override
            public long getItemId(int position) {
                return 0;
            }

            @Override
            public View getView(int i, View view, ViewGroup viewGroup) {
                View  meinView = getLayoutInflater().inflate(R.layout.list_item, null);
                TextView textsong = meinView.findViewById(R.id.txtsongname);
                textsong.setSelected(true);
                textsong.setText(items[i]);
                return meinView;
            }
        }

        customAdapter customAdapter = new customAdapter();
        listView.setAdapter(customAdapter);


        listView.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> parent, View view, int position, long id) {
                String songName = (String) listView.getItemAtPosition(position);
                startActivity(new Intent(getApplicationContext(), PlayerActivity.class)
                        .putExtra("songs", mySongs)
                        .putExtra("songname", songName)
                        .putExtra("pos", position));                                     // songs, songname ,und pos Als schl√ºssel  in PlayerActivity.class
            }
        });




}}